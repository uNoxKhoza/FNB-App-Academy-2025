# tuples
# tuples are unchangable , you can't add or remove
# tuples are suitable for situations where you want to store a collection of elements..

# create tuple containing even numbers
my_tuples = (2,4,6,8,10)
print(my_tuples)

# Print the fourth element(index 3) of the tuple
print(my_tuples[3])

# Print the last element of the negetive indexing
print(my_tuples[-1])

tuple1 =(1,2,3)
tuple2 = (4,5,6)

# Concatenate the two tuples to form a new tuple
conc_tuple = tuple1 + tuple2
print(conc_tuple)

# Repeat tuple 5 times using * operator
rep_tuple = tuple1 * 5
print(rep_tuple)